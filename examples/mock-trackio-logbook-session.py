#!/usr/bin/env python3
"""Create a realistic local Trackio logbook by repeatedly calling the CLI.

The script builds a fresh mock reproduction workspace, creates a logbook, and
adds notebook-style markdown, code, figure, and command-run cells. It is meant
to execute immediately as a demo of what an agent-maintained paper reproduction
logbook might look like.
"""

from __future__ import annotations

import argparse
import json
import shlex
import shutil
import socket
import subprocess
import sys
import textwrap
from pathlib import Path

ROOT = Path(__file__).resolve().parent
DEFAULT_WORKSPACE = ROOT / "mock_logbook_session"
MARKER = ".mock_trackio_logbook_workspace"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Mock a full Trackio logbook session using the trackio CLI."
    )
    parser.add_argument(
        "--workspace",
        type=Path,
        default=DEFAULT_WORKSPACE,
        help=f"Directory to create/use. Default: {DEFAULT_WORKSPACE.name}",
    )
    parser.add_argument(
        "--keep-existing",
        action="store_true",
        help="Append to an existing generated workspace instead of resetting it.",
    )
    parser.add_argument(
        "--trackio",
        default=shutil.which("trackio") or "trackio",
        help='Trackio command to run, e.g. "trackio" or "python -m trackio.cli".',
    )
    return parser.parse_args()


def reset_workspace(workspace: Path, keep_existing: bool) -> None:
    workspace = workspace.resolve()
    if workspace.exists() and not keep_existing:
        marker = workspace / MARKER
        if not marker.exists():
            raise SystemExit(
                f"Refusing to delete {workspace}: it was not created by this script. "
                f"Use --workspace with an empty/generated directory or --keep-existing."
            )
        shutil.rmtree(workspace)
    workspace.mkdir(parents=True, exist_ok=True)
    (workspace / MARKER).write_text("generated by mock_trackio_logbook_session.py\n")


def run_cli(
    trackio: list[str], workspace: Path, *args: str
) -> subprocess.CompletedProcess[str]:
    command = [*trackio, "logbook", *args]
    print("\n$ " + " ".join(_shell_quote(part) for part in command))
    result = subprocess.run(
        command,
        cwd=workspace,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        check=False,
    )
    if result.stdout:
        print(result.stdout.rstrip())
    if result.returncode != 0:
        raise SystemExit(f"Command failed with exit code {result.returncode}")
    return result


def refresh_static_site(trackio: list[str], workspace: Path) -> None:
    """Run the preview command briefly so logbook.json/logbook.md are refreshed."""
    with socket.socket() as sock:
        sock.bind(("127.0.0.1", 0))
        port = sock.getsockname()[1]

    command = [*trackio, "logbook", "serve", "--no-browser", "--port", str(port)]
    print("\n$ " + " ".join(_shell_quote(part) for part in command))
    try:
        result = subprocess.run(
            command,
            cwd=workspace,
            text=True,
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            timeout=2,
            check=False,
        )
    except subprocess.TimeoutExpired as exc:
        output = exc.stdout or ""
        if output:
            print(output.rstrip())
        print("Stopped preview refresh.")
        return

    if result.stdout:
        print(result.stdout.rstrip())
    if result.returncode != 0:
        raise SystemExit(f"Command failed with exit code {result.returncode}")


def _shell_quote(value: str) -> str:
    if not value:
        return "''"
    safe = set("abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789_-./:=,")
    if all(ch in safe for ch in value):
        return value
    return "'" + value.replace("'", "'\"'\"'") + "'"


def write_file(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(textwrap.dedent(text).strip() + "\n", encoding="utf-8")


