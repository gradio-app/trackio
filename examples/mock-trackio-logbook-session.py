def build_logbook(workspace: Path, trackio: list[str]) -> None:
    py = sys.executable

    run_cli(trackio, workspace, "open", "--title", "Mock CoT Reproduction Logbook")

    run_cli(trackio, workspace, "page", "Campaign overview")
    run_cli(
        trackio,
        workspace,
        "cell",
        "markdown",
        (
            "Goal: reproduce the main chain-of-thought and self-consistency claims on "
            "a small GSM8K-style slice, then document which deltas survive under the "
            "same prompt budget. Planned pages cover baselines, sampling, error "
            "analysis, and final takeaways."
        ),
        "--title",
        "Reproduction plan",
        "--page",
        "Campaign overview",
        "--link",
        "https://arxiv.org/abs/2201.11903",
        "--link",
        "https://github.com/openai/grade-school-math",
    )
    run_cli(
        trackio,
        workspace,
        "cell",
        "markdown",
        (
            "| Experiment | Status | Decision |\n"
            "| --- | --- | --- |\n"
            "| Direct baseline | done | Use as floor for prompt formatting checks. |\n"
            "| CoT baseline | done | Primary target to beat. |\n"
            "| Self-consistency | in progress | Promising but cost-heavy. |\n"
            "| Error buckets | planned | Needed before deciding next ablation. |"
        ),
        "--title",
        "Campaign state table",
        "--page",
        "Campaign overview",
        "--artifact",
        "cot-repro/campaign-plan:v1",
    )

    run_cli(trackio, workspace, "page", "Baselines")
    run_cli(
        trackio,
        workspace,
        "cell",
        "markdown",
        (
            "The direct-answer prompt landed at 29.7% exact match. The CoT prompt "
            "improved to 41.4%, but parse failures rose because several rationales "
            "ended without a clean final answer marker."
        ),
        "--title",
        "Baseline result",
        "--page",
        "Baselines",
        "--code",
        "configs/baseline.json",
        "--code",
        "scripts/run_baseline.py",
        "--link",
        "https://huggingface.co/datasets/openai/gsm8k",
    )
    run_cli(
        trackio,
        workspace,
        "run",
        "--page",
        "Baselines",
        "--title",
        "Execute baseline harness",
        "--link",
        "https://huggingface.co/jobs/mock-org/cot-baseline-001",
        "--",
        py,
        "scripts/run_baseline.py",
    )
    run_cli(
        trackio,
        workspace,
        "cell",
        "code",
        "--title",
        "Parse final answer helper",
        "--page",
        "Baselines",
        "--code-text",
        (
            "def extract_final_answer(text):\n"
            "    marker = 'Therefore, the answer is'\n"
            "    if marker not in text:\n"
            "        return None\n"
            "    return text.split(marker, 1)[1].strip().rstrip('.')"
        ),
        "--language",
        "python",
        "--output",
        "Validated on 50 sampled generations: 46 parsed, 4 missing marker.",
    )

    run_cli(trackio, workspace, "page", "Self-consistency")
    run_cli(
        trackio,
        workspace,
        "run",
        "--page",
        "Self-consistency",
        "--title",
        "Run 20-sample majority vote",
        "--link",
        "https://huggingface.co/spaces/mock-org/cot-trackio-dashboard",
        "--",
        py,
        "scripts/run_self_consistency.py",
    )
    run_cli(
        trackio,
        workspace,
        "cell",
        "markdown",
        (
            "Self-consistency reached 56.3% exact match, a +14.9 point gain over "
            "single-sample CoT. The run is not yet the preferred recipe because it "
            "costs 18.6x more tokens on this slice."
        ),
        "--title",
        "Sampling tradeoff",
        "--page",
        "Self-consistency",
        "--code",
        "configs/self_consistency.json",
        "--artifact",
        "cot-repro/self-consistency-generations:v2",
    )
    run_cli(
        trackio,
        workspace,
        "cell",
        "figure",
        "--title",
        "Accuracy comparison",
        "--page",
        "Self-consistency",
        "--html",
        "figures/accuracy_bars.html",
        "--raw",
        "results/accuracy_by_condition.csv",
    )

    run_cli(trackio, workspace, "page", "Error analysis")
    run_cli(
        trackio,
        workspace,
        "run",
        "--page",
        "Error analysis",
        "--title",
        "Bucket failed CoT generations",
        "--",
        py,
        "scripts/eval_error_buckets.py",
    )
    run_cli(
        trackio,
        workspace,
        "cell",
        "figure",
        "--title",
        "Failure bucket sketch",
        "--page",
        "Error analysis",
        "--html-text",
        (
            "<div style='font-family: system-ui'>"
            "<strong>Failure buckets</strong>"
            "<ul><li>Arithmetic slip: 18</li><li>Wrong equation: 11</li>"
            "<li>Answer extraction: 7</li><li>Ambiguous question: 3</li></ul>"
            "</div>"
        ),
        "--raw-text",
        json.dumps(
            {
                "arithmetic slip": 18,
                "wrong equation": 11,
                "answer extraction": 7,
                "ambiguous question": 3,
            },
            indent=2,
        ),
    )
    run_cli(
        trackio,
        workspace,
        "cell",
        "markdown",
        (
            "Decision: next iteration should test a calculator-style verifier before "
            "more sampling. Arithmetic slips are the largest bucket, so additional "
            "samples may repeat the same systematic error."
        ),
        "--title",
        "Next experiment decision",
        "--page",
        "Error analysis",
    )

    run_cli(trackio, workspace, "page", "Ablations", "--parent", "self-consistency")
    run_cli(
        trackio,
        workspace,
        "cell",
        "code",
        "--title",
        "Ablation TODO generator",
        "--page",
        "Ablations",
        "--code-text",
        (
            "ablations = ['5 samples', '10 samples', '20 samples', 'verifier rerank']\n"
            "for ablation in ablations:\n"
            "    print(f'queue: {ablation}')"
        ),
        "--language",
        "python",
        "--output",
        "queue: 5 samples\nqueue: 10 samples\nqueue: 20 samples\nqueue: verifier rerank",
    )

    run_cli(trackio, workspace, "read", "pages", "--json")
    run_cli(trackio, workspace, "read", "page", "Self-consistency")
    refresh_static_site(trackio, workspace)


def main() -> None:
    args = parse_args()
    workspace = args.workspace.resolve()
    trackio = shlex.split(args.trackio)
    if not trackio:
        raise SystemExit("--trackio cannot be empty")
    reset_workspace(workspace, keep_existing=args.keep_existing)
    seed_repro_files(workspace)
    build_logbook(workspace, trackio)

    logbook_dir = workspace / ".trackio" / "logbook"
    print("\nMock logbook created.")
    print(f"Workspace: {workspace}")
    print(f"Logbook files: {logbook_dir}")
    print(f"Flattened agent view: {logbook_dir / 'logbook.md'}")
    open_command = (
        f"cd {_shell_quote(str(workspace))} && "
        f"{' '.join(_shell_quote(part) for part in trackio)} logbook serve"
    )
    print(f"Open logbook with: {open_command}")


if __name__ == "__main__":
    main()
